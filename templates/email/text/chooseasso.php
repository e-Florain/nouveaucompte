<br>
* Le 1 % pour les associations, qu'est-ce que c'est ?<br>
Quand une personne adhère au Florain, elle choisit une association locale, membre du Florain, qu'elle souhaite soutenir. Ainsi, à chaque change d'euros en florains, un don équivalent à 1% de la somme changée sera versé à l'association retenue. Par exemple, en changeant 300 euros tout au long de l'année, je recevrai 300 florains, et l'association que j'ai choisie recevra 3 florains en fin d'année !<br>
